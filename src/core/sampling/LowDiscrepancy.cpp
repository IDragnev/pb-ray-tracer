    template <int Base>
    static Float radicalInverseSpecialized(std::uint64_t num) {
        static_assert(Base > 0);

        const Float invBase = 1.f / static_cast<Float>(Base);

        std::uint64_t reversedDigits = 0;
        Float invBaseN = 1.f;

        while (num > 0) {
            const std::uint64_t next = num / Base;
            const std::uint64_t digit = num - next * Base;

            reversedDigits = reversedDigits * Base + digit;
            invBaseN *= invBase;

            num = next;
        }

        assert(reversedDigits * invBaseN < 1.00001);

        return std::min(reversedDigits * invBaseN,
                        rng::constants::OneMinusEpsilon);
    }

